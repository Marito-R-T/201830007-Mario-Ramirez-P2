    private void btnCargarArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarArchivoActionPerformed
        JFileChooser jf = new JFileChooser();//Sirve para elegir el archivo de formato txt

        jf.setAcceptAllFileFilterUsed(false);
        FileFilter txt = new FileNameExtensionFilter("Archivo de texto (.txt)", "txt");
        jf.setFileFilter(txt);
        jf.showOpenDialog(this);
        File file = jf.getSelectedFile();
        if (file != null) {
            cargarArchivo cargador = new cargarArchivo();
            String codigo = cargador.cargar(file);
            archivoVista.setText(codigo);
        }
    }//GEN-LAST:event_btnCargarArchivoActionPerformed

